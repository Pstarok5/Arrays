// push array
let names = ["Dad", "Mom", "Sister", "Uncle"];
names.push("Nephew");
console.log(names);


// pop array
let fruits = ["Mango", "Pawpaw", "Grape", "Apple"];
fruits.pop();
console.log(fruits);


// unshift array
let car = ["Lambo", "Ferrarri", "Maybach", "G-wagon"];
car.unshift("Royce-rolls");
console.log(car);


// shift array
let girls = ["Chidinma", "Ifunanya", "Rose", "Mary"];
girls.shift();
console.log(girls);


// Join array
let boys = ["Daniel", "Emmanuel", "Chibuike", "Habeeb"];
console.log(boys.join(""));


// splice array
let watch = ["Ice-watch", "Rolex-watch", "Silver-watch", "Gold-watch"];
watch.splice(2,0, "Rubber")
console.log(watch);


// slice array
let foods = ["Beans", "Rice", "Indomie", "Garri"];
console.log(foods.slice(0));



